package loops_prob;

public class Ram_Flag {

	public static void main(String[] args) {
		
		for(int i=5;i>=1;i--) {
			System.out.println("\n");
			for(int j=1;j<=i;j++) {
				System.out.print("\tram");
			}

	}
		for(int a=1;a<=5;a++) {
			System.out.println("\n");
			for(int b=1;b<=a;b++) {
				System.out.print("\tram");
			}
		}
		for(int m=1;m<=5;m++) {
			System.out.println("\n\tram");
		}

}
}
